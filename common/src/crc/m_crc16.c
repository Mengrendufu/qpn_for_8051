uint16_t crc16_ccitt_false(uint8_t *arr,
    uint16_t arr_size)
{
    uint8_t *crc_arr;
    uint16_t crc_arr_len;
    uint16_t crc;

    crc_arr = arr;
    crc_arr_len = arr_size;
    crc = INIT_CRC16_CCITT_FALSE;

    while (crc_arr_len--) {
        crc = update_crc16_ccitt_false(crc,
            *crc_arr++);
    }

    return crc ^ XOROUT_CRC16_CCITT_FALSE;
}
