uint16_t update_crc16_ccitt_false(uint16_t pre_crc,
    uint8_t ch)
{
    uint8_t crc_idx;
    uint16_t crc;

    crc_idx = (uint8_t)(((((uint16_t)pre_crc) & 0xFF00) >> 8)
        ^ (((uint16_t)ch) & 0x00FF));

    crc = ((((uint16_t)pre_crc) & 0x00FF) << 8)
        ^ table_crc16_ccitt_false[crc_idx];

    return crc;
}
